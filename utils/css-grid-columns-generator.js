const fs = require('fs');
const path = require('path');

const outputFile = path.join(__dirname, '../src/styles/grid/grid-columns.css');

fs.exists(outputFile, (exists) => {
    if (exists) {
        fs.unlinkSync(outputFile);
    }
    const stream = fs.createWriteStream(outputFile);
    stream.write(`/* 
!!!! Autogenerated !!!! 
*/    
`);
    
    for (let i = 1; i < 13; i++) {
        stream.write(`
.col-start-${i} {
    grid-column-start: ${i};
}
.col-end-${i} {
    grid-column-end: ${i};
}`);
    }
    for (let i = 1; i < 13; i++) {
        stream.write(`
.row-start-${i} {
    grid-row-start: ${i};
}
.row-end-${i} {
    grid-row-end: ${i};
}`);
    }
    stream.end();
});
